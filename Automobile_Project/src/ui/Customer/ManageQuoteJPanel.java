    private void btnAcceptActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAcceptActionPerformed
        // TODO add your handling code here:
         int selectedRow = tblWorkArea.getSelectedRow();

        if (selectedRow < 0){
            JOptionPane.showMessageDialog(this, "Pick a selected row", "Information", JOptionPane.INFORMATION_MESSAGE);

            return;
        }
        QuoteOrderRequest req = (QuoteOrderRequest)tblWorkArea.getValueAt(selectedRow, 0);
        if (req.getIsQuoteReq()==true && req.getIsQuoteRes()==true && req.getIsPriceAcceptable()==false ){
            req.setStatus("Price confirmed & order placed");
            req.setIsPriceAcceptable(true);
            JOptionPane.showMessageDialog(this, "Price confirmed & order placed", "Information", JOptionPane.INFORMATION_MESSAGE);
            populateRequestTable();
            btnAccept.setEnabled(false);
            return;
        }
        else if(req.getIsQuoteReq()==true && req.getIsQuoteRes()==true && req.getIsPriceAcceptable()==true){
            JOptionPane.showMessageDialog(this, "Price is confirmed and order placed!!", "Information", JOptionPane.INFORMATION_MESSAGE);
            return;
        }
       populateRequestTable();
        
        
    }//GEN-LAST:event_btnAcceptActionPerformed

    private void btnViewOrderActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnViewOrderActionPerformed
        // TODO add your handling code here:
        int selectedRow = tblWorkArea.getSelectedRow();

        if (selectedRow < 0){
            JOptionPane.showMessageDialog(this, "Pick a selected row", "Information", JOptionPane.INFORMATION_MESSAGE);

            return;
        }

        QuoteOrderRequest req = (QuoteOrderRequest)tblWorkArea.getValueAt(selectedRow, 0);

        ViewCarDetailsJPanel viojp = new ViewCarDetailsJPanel(workArea, req);
        workArea.add("ViewCarDetailsJPanel",viojp);
        CardLayout layout = (CardLayout) workArea.getLayout();
        layout.next(workArea);
    }//GEN-LAST:event_btnViewOrderActionPerformed

